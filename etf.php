                                <p>En el mundo existe una gran variedad de índices que actúan como "termómetros" del desempeño de un mercado, sector, país, región, etc. Los "ETF" - Exchange Traded Funds, o TRAC- son instrumentos de inversión que permiten replicar un índice. Un ETF es lo mejor de dos mundos: la diversificación que ofrecen los fondos sin tener que adquirir todos los activos que lo componen y la posibilidad de operarlo bajo las mismas condiciones que una acción en la Bolsa Mexicana de Valores. De esta forma, los inversionistas pueden tener acceso a un índice o portafolios de acciones por medio de una misma transacción y operar en tiempo real a lo largo de la jornada bursátil. Los ETF de Quanta Shares, Dólar Trac y Peso Trac, tienen la aprobación en exclusiva de la CNBV y buscan emular índices del rendimiento diario del tipo de cambio (BMV MXP-USD y BMV USD-MXP), por lo que sus portafolios están diseñados para reproducirlos mediante CETES y Futuros del Tipo de Cambio. La cartera que compone a un ETF es muy similar a un fondo indizado, el cual busca obtener los mismos rendimientos que el índice que replica, con la ventaja de que opera en tiempo real.</p>
